# Посчитайте количество согласных букв в данной строке.

#text = "Мама мыла раму" #количество согласных - 6, гласных - 6
text = "Шла Саша по шоссе" #количество согласных - 8, гласных - 6
i = 0

for letter in text:
    if letter not in ("а", "о", "э", "у", "и", "ы", "ё", "е", "ю", "я", " "):
        i += 1

print("Количество согласных в тексте:", i)
