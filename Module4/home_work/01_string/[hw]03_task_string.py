text = "Таким образом сложившаяся структура организации требует от нас системного анализа системы обучения кадров"
consonants = "б, в, г, д, ж, з, й, к, л, м, н, п, р, с, т, ф, х, ц, ч, ш, щ"
cons=consonants.replace(", ","")
i=0
tot=0
while i<len(cons):
    tot+=text.lower().count(cons[i])
    i+=1
