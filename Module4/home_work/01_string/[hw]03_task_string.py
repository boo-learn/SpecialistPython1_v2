# Посчитайте количество согласных букв в данной строке.

text = "Программист"

consonants = "бвгджзйклмнпрстфхцчшщ"  # Список согласных букв
count = 0  # счетчик

for word in text:  # Итерация по каждой букве в строке
    # Если согласная, счетчик + 1
    if word.lower() in consonants:
        count += 1

print("Количество согласных букв в строке:", count)
