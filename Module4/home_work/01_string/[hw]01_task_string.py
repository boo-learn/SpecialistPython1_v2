# Дан произвольный текст. Каких символов больше, точек "." или запятых ","?
# Если точек и запятых одинаково - выведите "одинаково"

text = 'Дан произвольный текст. Каких символов больше, точек "." или запятых ","? Если точек и запятых одинаково - выведите "одинаково"'
if text.count(".") < text.count(","):
    print("больше запятых")
elif text.count(".") > text.count(","):
    print("больше точек")
else:
    print("одинаково")
