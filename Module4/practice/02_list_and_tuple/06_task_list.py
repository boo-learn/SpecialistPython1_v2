# Даны два целых числа.
# Вывести список всех чисел кратных трем в диапазоне между заданными числами.

a = int(input("Первое число: "))  # Первое число
b = int(input("Второе число: "))  # Второе число

list_3 = []

if b <= a:
    a, b = b, a

while a <= b:
    if a % 3 == 0:
        list_3.append(a)
    a += 1

print(list_3)
